
let number = 4;

console.log("Starting while loop")
while(number <= 5) {
    console.log(`The number is ${number}`)
    number++;
}

console.log("After while loop\n")

number = 4
console.log("Starting do-while loop")
do {
    console.log(`The number is ${number}`)
    number++;
} while(number <= 5);

console.log("After do-while loop")